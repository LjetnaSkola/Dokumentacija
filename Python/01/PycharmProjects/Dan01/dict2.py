def process_dictionary(dictionary):
    # Print original dictionary
    print(dictionary)

    # Create set of keys
    key_set = set(dictionary.keys())
    print("Set of keys:", key_set)

    # Create list of values
    value_list = list(dictionary.values())
    print("List of values:", value_list)

